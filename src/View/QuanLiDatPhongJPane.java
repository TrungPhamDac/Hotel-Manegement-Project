    public void showTableKhachHang()
    {
        for (KhachHang kh : listKhachHang)
        {
            tblKhachHang.addRow(new Object[]{kh.getMaKH(),kh.getTenKH(),kh.getCCCD(),kh.getSDT()});
        }
    }
